    private void selectPath() {
        JFileChooser chooser = new JFileChooser();

        File dir = new File(outPath.getText());
        if (!dir.exists())
            dir = new File(".");

        chooser.setCurrentDirectory(dir);
        chooser.setDialogTitle("Choose Output Directory...");
        chooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        chooser.setAcceptAllFileFilterUsed(false);

        if (chooser.showOpenDialog(null) == JFileChooser.APPROVE_OPTION) {
            outPath.setText(chooser.getSelectedFile().getAbsolutePath().toString());
        }
    }

    private void selectFile() {
        JFileChooser chooser = new JFileChooser();

        File dir = new File(inPath.getText());
        if (!dir.exists())
            dir = new File(".");

        chooser.setCurrentDirectory(dir);
        chooser.setDialogTitle("Choose input File...");


        FileNameExtensionFilter filter = new FileNameExtensionFilter("VCF Files", "vcf", "VCF");
        chooser.setFileFilter(filter);

        if (chooser.showOpenDialog(null) == JFileChooser.APPROVE_OPTION) {
            inPath.setText(chooser.getSelectedFile().getAbsolutePath().toString());
        }
    }


    private void onOK() {
        buttonSplit.setEnabled(false);
        logArea.setText("");

        class VcfSplitterInteractice extends VcfSplitter {
            public VcfSplitterInteractice() {
                super();
            }

            @Override
            protected void log(Level level, String text) {
                logArea.setText(logArea.getText() + text + "\n");
            }
        }

        VcfSplitter dBvcfSplitter = new VcfSplitterInteractice();
        dBvcfSplitter.split(inPath.getText(), outPath.getText());
    }

    private void onCancel() {
        dispose();
    }

    public static void main(String[] args) {
        VcfDialog dialog = new VcfDialog();
        dialog.pack();
        dialog.setVisible(true);
        System.exit(0);
    }

